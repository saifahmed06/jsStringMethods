// The localeCompare() method checks if a given string comes before, after, or is equivalent as another string in sort order.

// Example
// comparing 'c' with 'b' using localeCompare()
let result1 = 'c'.localeCompare('b');

// returns positive value if 'b' comes before 'c'
console.log(result1);

// Output: 
// 1 